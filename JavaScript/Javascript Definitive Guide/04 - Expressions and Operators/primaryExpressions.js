
/* 
An expression is a phrase of JavaScript that can be evaluated to produce a value.
*/

// PRIMARY EXPRESSIONS

// 1 - Literal Values

let x;

x = 1.23 // A number literal
x = "hello" // A string literal
x = /pattern/ // A regular expression literal


// 2 - Certain Language Keywords

x = true // Evalutes to the boolean true value
x = false // Evaluates to the boolean false value
x = null // Evaluates to the null value
x = this // Evaluates to the "current" object

// 3 - Variable References

let i = 0; // Evaluates to the value of the variable i.
let sum = 0; // Evaluates to the value of the variable sum.
let k = undefined; // The value of the "undefined" property of the global object => Reference of Global Object